package com.array.java;

public class ArrayAvg
{

	public static void main(String[] args)
	{
        int[] arr = new int[5];
        arr[0] =10;
        arr[1] =20;
        arr[2] =30;
        arr[3] =40;
        arr[4] = 50;
        
        int avg = (arr[0]+arr[1]+arr[2]+arr[3]+arr[4])/5;
        System.out.println(avg);
        
	}

}
